public class Function {
	private int price;
	private String function;

	public void setPrice(int price) {
		this.price = price;
	}

	public void setFunction(String function) {
		this.function = function;
	}

	public int getPrice() {
		return price;
	}

	public String getFunction() {
		return function;
	}
}
